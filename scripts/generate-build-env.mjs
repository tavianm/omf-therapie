#!/usr/bin/env node
/**
 * Pre-build step: snapshot build-time-only Netlify env vars into a runtime-
 * readable module so Lambda functions (API routes + cron functions) can
 * access them.
 *
 * WHY THIS EXISTS
 * ───────────────
 * Netlify pre-defines `CONTEXT` ("production" | "deploy-preview" |
 * "branch-deploy") and `COMMIT_REF` (git SHA) at BUILD time only. They are
 * NOT injected into the function runtime `process.env` by default — see
 * Netlify docs: "Build environment variables" vs "Functions environment
 * variables". Only operator-configured vars (dashboard / netlify.toml
 * [build.environment]) reach the runtime.
 *
 * Consequence (production bug observed 2026-07-19):
 *   - Crons emitted `environment: 'staging'` in prod because
 *     `process.env.CONTEXT === undefined` → fallback 'staging'.
 *   - Server deploy canary emitted `deploy: unknown` because
 *     `process.env.COMMIT_REF === undefined` → fallback 'unknown'.
 *
 * HOW THIS FIXES IT
 * ─────────────────
 * Reads the two build-time vars and writes them as a frozen, typed module
 * (`netlify/functions/_lib/build-env.ts`). esbuild bundles that module into
 * each cron, so the values become literal strings at the cron runtime — no
 * `process.env` lookup needed.
 *
 * For Astro server routes (Vite-bundled), the same vars are exposed via
 * `import.meta.env.PUBLIC_*` (the PUBLIC_ prefix lets Astro/Vite inline
 * them into server code too), which `src/lib/build-env.ts` reads. Both
 * paths resolve to the same values.
 *
 * INVARIANTS
 * ──────────
 * - Local dev (`npm run dev` / `npm run build` outside Netlify): both vars
 *   are absent → CONTEXT defaults to 'development', COMMIT_REF to 'dev'.
 *   The Sentry environment is then 'staging' (not 'development'), matching
 *   the existing fallback so local events don't pollute production. This
 *   matches the prior behavior.
 * - The generated file MUST be gitignored (contains per-build SHA) AND be
 *   present before `astro build` / cron bundling runs. So it's generated
 *   in `prebuild` (runs before every `npm run build`).
 */

import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

const context = process.env.CONTEXT ?? 'development';
const commitRef = process.env.COMMIT_REF ?? 'dev';

const target = join(repoRoot, 'netlify', 'functions', '_lib', 'build-env.ts');

// Defensive: never emit unescaped shell chars. CONTEXT is one of a small
// fixed set, COMMIT_REF is a 40-char hex SHA from git. Both are safe to
// inline as TS string literals, but we still JSON.stringify (which handles
// quotes/backslashes) as a belt-and-suspenders.
const content = `/**
 * ⚠️  AUTO-GENERATED by scripts/generate-build-env.mjs at build time.
 * DO NOT EDIT — changes will be overwritten on the next \`npm run build\`.
 *
 * Snapshots Netlify's build-only env vars (CONTEXT, COMMIT_REF) so they
 * survive into the function runtime. See the generator script for the full
 * rationale (Netlify does NOT expose these at runtime by default).
 *
 * Generated for: CONTEXT=${JSON.stringify(context)} COMMIT_REF=${JSON.stringify(commitRef)}
 */

export const BUILD_CONTEXT: string = ${JSON.stringify(context)};
export const BUILD_COMMIT_REF: string = ${JSON.stringify(commitRef)};
`;

await mkdir(dirname(target), { recursive: true });
await writeFile(target, content, 'utf8');

console.log(
  `[build-env] wrote ${target.replace(repoRoot + '/', '')} ` +
    `(CONTEXT=${context}, COMMIT_REF=${commitRef})`,
);
